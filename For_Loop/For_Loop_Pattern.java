package For_Loop;

public class For_Loop_Pattern {
    public static void main(String bca[]) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j--) {
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
